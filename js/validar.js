export function valida(input) {
    const tipoDeInput = input.dataset.tipo

    if(validadores[tipoDeInput]) {
        validadores[tipoDeInput](input)
    }

    if(input.validity.valid) {
        input.parentElement.classList.remove('dado-errado')
        input.parentElement.querySelector('.icon-erro').innerHTML = ""
        input.parentElement.querySelector('.icon-ok').innerHTML = "&#9745"
        input.parentElement.querySelector('.input-mensagem-erro').innerHTML = ''
    } else {
        input.parentElement.classList.add('dado-errado')
        input.parentElement.querySelector('.icon-ok').innerHTML = ""
        input.parentElement.querySelector('.icon-erro').innerHTML = "&#9940"
        input.parentElement.querySelector('.input-mensagem-erro').innerHTML = mostraMensagemDeErro(tipoDeInput, input)
    }
}

